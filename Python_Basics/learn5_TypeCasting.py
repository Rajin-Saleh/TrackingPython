#  Learning TypeCasting in python

#  Conversion of one data type into another data type is typecasting
#  Explicit Conversion = Done by manual
#  Implicit Conversion = Done by Python


#  Explicit 
a = "1"
b = "2"
c = int(a) + int(b)
print(c)

#  Implicit TypeCasting

z = 1.9  # integer
y = 8    # float
x = z + y
print(x)
print("Type of x", type(x))


