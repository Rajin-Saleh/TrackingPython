# Learning If Else, elif statements in python

# Small if else program

name = input("What is your name? ").title()
print(f"{name}. Very good.")

age  = int(input("What is your age?"))
if (age >= 18):
    print(f"You are {age} years old.")
    print(f"It is an eligible age for driving.")
else:
    print(f"You are not eligible for driving license.\nPlease come when you are 18 or above.")











